/*
 Программа, демонстрирующая выведение значения 
 переменной на консоль
 */
package primery;

class Primery {
    public static void main(String[] args){
        float pi = 3.14f;//после значения типа float обязательно добавлять "f"
        System.out.println(pi);//выводит на консоль значение переменной  pi
    }
    
}
